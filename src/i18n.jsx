// ── Context ─────────────────────────────────────────────────────────────────────

const LangContext = createContext()

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => localStorage.getItem('neblina_lang') || 'pt')

  const setLang = useCallback((newLang) => {
    setLangState(newLang)
    localStorage.setItem('neblina_lang', newLang)
    document.documentElement.lang = newLang === 'pt' ? 'pt-BR' : newLang === 'zh' ? 'zh-CN' : newLang
  }, [])

  const t = useCallback((key, params = {}) => {
    let str = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.pt[key] ?? key
    // Replace {param} placeholders
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
    })
    return str
  }, [lang])

  return (
    <LangContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LangContext.Provider>
  )
}

export function useLang() {
  const ctx = useContext(LangContext)
  if (!ctx) throw new Error('useLang must be used within LangProvider')
  return ctx
}
